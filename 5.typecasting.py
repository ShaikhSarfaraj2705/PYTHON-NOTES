#TYPE CASTING/TYPE CONVERSION:conversion of one data type into another data type
#types of type-casting:1)explicit type-casting 2)implicit type-casting

#1)explicit type-casting:conversion of one data type into another data type,done via developer or programmer
a="65"
b="54"
print(a+b)
print(int(a)+int(b))
print("_________________________________________________________________________________")

#2)implicit type-casting:one data type is converted into other by python interpreter itself(automatically).
c=6.3
d=9
print(c+d)