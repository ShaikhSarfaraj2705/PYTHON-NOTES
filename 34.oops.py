#INTRODUCTION TO OBJECT-ORIENTED PROGRAMMING:the object-oriented programming(oop) in 
#python is to use classes and objects to represent real-world concepts and entities.

#A class is blueprint or template for creating objects. it defines the properties and 
#methods that an object of class will have.

#an object is an instance of class and it contains its own data and methods.

#one of the features of oop in python is encapsulation.which means that the internal state 
#of an object is hiden and can only be accessed or modified through the objects methods.

#another feature of oop in python is inheritance.which allows new classes to be created 
#that inherit the properties and methods  of an existing class.

#polymorphism also supperted in python,which means that object of different classes can be
#treated as if they were objects of a common class.